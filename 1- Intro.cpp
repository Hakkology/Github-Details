// Git nedir ?

// Git, yazılım geliştirme sürecinde kullanılan, kaynak kodunun versiyon kontrolünü sağlayan dağıtık bir sistemdir. 
// Linus Torvalds tarafından 2005 yılında Linux çekirdeği geliştirme ihtiyaçlarına yönelik olarak tasarlanmıştır. 
// Git'in temel amacı, bir projede çalışan geliştiriciler arasında kod değişikliklerinin etkili bir şekilde yönetilmesini sağlamaktır.

// Git'in Çalışma Prensibi

// Git, projenin tüm tarihçesini ve versiyon bilgilerini içeren bir depo (repository) üzerinde çalışır. 
// Bu depo, hem yerel (geliştiricinin kendi bilgisayarında) hem de uzak sunucularda (GitHub, GitLab, Bitbucket gibi hizmetler üzerinde) bulunabilir.

// Yerel Depo: Geliştiriciler, kendi bilgisayarlarında çalıştıkları projenin bir kopyasına sahiptirler. Bu kopya, tüm projenin tarihçesini ve dosyalarını içerir. 
// Geliştiriciler, bu yerel depoda çalışırken internet bağlantısına ihtiyaç duymazlar. 
// Değişiklikler, testler ve yeni özellikler ilk olarak yerel depoda yapıldıktan sonra uzak depoya aktarılır.

// Uzak Depo: Ekip üyelerinin kod üzerinde birlikte çalışabilmesi için kullanılan merkezi depodur. 
// Bir geliştirici yerel depodaki değişiklikleri uzak depoya gönderdiğinde (push), 
// diğer geliştiriciler bu değişiklikleri kendi yerel depolarına çekebilir (pull) ve üzerinde çalışmaya devam edebilirler.

// Git'in Özellikleri
// Dağıtık Yapı: Git, her geliştiricinin projenin tam bir kopyasına sahip olduğu dağıtık bir yapıya sahiptir. 
// Bu, ağ bağlantısı olmadan çalışmayı mümkün kılar ve merkezi bir arıza noktası olmamasını sağlar.
// Hız ve Verimlilik: Git, projenin tarihçesini ve dosya durumlarını hızlı ve verimli bir şekilde yönetebilir. 
// Büyük projelerde bile performansından ödün vermez.
// Esneklik: Git, çok sayıda iş akışı modelini destekler; örneğin, merkezi ve dağıtık modeller. 
// Geliştiriciler, projenin ihtiyaçlarına göre en uygun iş akışını seçebilirler.

// Nerede Çalışır?

// Git yazılımı, Linux, macOS ve Windows dahil olmak üzere çeşitli işletim sistemlerinde çalışabilir. Kurulumu ve kullanımı kolaydır. 
// Geliştiriciler, komut satırı aracılığıyla veya çeşitli grafik kullanıcı arayüzleri (GUI'ler) üzerinden Git işlemlerini gerçekleştirebilirler.
// Git ve uzak depolar (GitHub, GitLab vb.) arasındaki etkileşim, genellikle HTTPS veya SSH protokolleri üzerinden gerçekleşir, 
// bu da işlemlerin güvenli bir şekilde yapılmasını sağlar.
// Sonuç olarak, Git'in esnekliği, güvenliği ve dağıtık yapısı, onu modern yazılım geliştirme projelerinde vazgeçilmez bir araç haline getirmiştir. 
// Geliştiriciler arasında kod paylaşımını ve işbirliğini kolaylaştırarak, yazılım geliştirme sürecinin daha verimli ve yönetilebilir olmasına olanak tanır.