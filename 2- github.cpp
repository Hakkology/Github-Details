
// GitHub Nedir?

// GitHub, yazılım geliştiricilerin projelerini barındırmak, yönetmek ve takip etmek için kullandıkları bir web tabanlı arayüzdür. 
// Git versiyon kontrol sistemini temel alır ve bu sistem üzerine kurulu birçok ek özellik sunar. 
// GitHub, bireysel geliştiricilerden büyük kuruluşlara kadar her ölçekteki kullanıcıya hitap eder ve 
// açık kaynak projelerden özel iş birliklerine kadar geniş bir yelpazede kullanım imkanı sağlar.

// GitHub'ın Özellikleri

//     Repositoriler (Depolar): GitHub, her bir proje için bir depo sağlar. 
//     Bu depolar, proje dosyalarını, dokümantasyonu, sorun takibini, kullanıcı katkılarını ve daha fazlasını içerebilir.

//     Fork ve Pull Requestler: Kullanıcılar, başkalarının projelerini "fork"layabilir (kopyalayabilir) ve 
//     kendi değişikliklerini yaptıktan sonra bu değişiklikleri orijinal projeye katkıda bulunmak için "pull request" açabilir. 
//     Bu, açık kaynak geliştirme sürecinin temel bir parçasıdır.

//     Sorun İzleyici (Issue Tracker): Projelerle ilgili hatalar, özellik istekleri ve diğer sorunlar için bir izleyici sunar. 
//     Bu, kullanıcıların projeler üzerinde işbirliği yapmasını ve gelişimini takip etmesini kolaylaştırır.

//     GitHub Actions: Otomatikleştirilmiş iş akışları oluşturmak için kullanılır. 
//     Bu, yazılım geliştirme süreçlerinde testlerin otomatik olarak çalıştırılması, derlemelerin yapılması ve dağıtımların yönetilmesi gibi görevleri içerebilir.

//     GitHub Pages: Kullanıcıların ve projelerin statik web sitelerini barındırmasına olanak tanır. 
//     Bu özellikle projelerin dokümantasyonu, kişisel portföyler ve proje anasayfaları için kullanışlıdır.

//     Ekip İşbirliği Araçları: Ekiplerin ve organizasyonların projeler üzerinde etkili bir şekilde işbirliği yapmalarını sağlayan özellikler içerir. 
//     Bu, ekip üyelerinin rollerini yönetmek, projelere erişim kontrolü uygulamak ve iş akışlarını düzenlemek için kullanılır.
